function somar(valor1,valor2){
    return valor1+valor2
}
function subitrair(valor1,valor2){
    return valor1-valor2
}
function dividir(valor1,valor2){
    return valor1/valor2
}
function multiplicacao(valor1,valor2){
    return valor1*valor2
}

module.exports ={somar,subitrair,dividir,multiplicacao} ;